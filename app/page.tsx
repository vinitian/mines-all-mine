"use client";

import { useEffect, useState } from "react";
import { socket } from "../socket";

interface Message {
  userID: string;
  text: string;
  timestamp: string;
}
export default function Home() {
  const [isConnected, setIsConnected] = useState(false);
  const [transport, setTransport] = useState("N/A");

  const [message, setMessage] = useState("");
  const [messages, setMessages] = useState<Message[]>([]);

  useEffect(() => {
    if (socket.connected) {
      onConnect();
    }

    function onConnect() {
      setIsConnected(true);
      setTransport(socket.io.engine.transport.name);

      socket.io.engine.on("upgrade", (transport) => {
        setTransport(transport.name);
      });
    }

    function onDisconnect() {
      setIsConnected(false);
      setTransport("N/A");
    }

    socket.on("connect", onConnect);
    socket.on("disconnect", onDisconnect);

    return () => {
      socket.off("connect", onConnect);
      socket.off("disconnect", onDisconnect);
    };
  }, []);

  useEffect(() => {
    // Listen for messages from the server
    socket.on("message", (msg: string) => {
      setMessages((prev) => [
        ...prev,
        {
          userID: socket.id!,
          text: msg,
          timestamp: Date(),
        },
      ]);
    });

    return () => {
      socket.disconnect();
    };
  }, []);

  const sendMessage = () => {
    socket.emit("message", message); // Send message to server
    setMessages((prev) => [
      ...prev,
      {
        userID: socket.id!,
        text: message,
        timestamp: Date(),
      },
    ]); // Add your message to the chat
    setMessage(""); // Clear input field
  };

  const handleKeyDown = (e: React.KeyboardEvent<HTMLInputElement>) => {
    if (e.key === "Enter") {
      sendMessage();
    }
  };

  return (
    // todo: detect dark-light mode of user
    <div className="m-8">
      <h1 className="text-title">Real-Time Chat</h1>
      <div>
        {messages.map((msg, index) => (
          <div key={index}>
            {msg.userID}: {msg.text} ({msg.timestamp})
          </div>
        ))}
      </div>
      <div className="flex gap-4">
        <input
          value={message}
          onChange={(e) => setMessage(e.target.value)}
          onKeyDown={handleKeyDown}
          placeholder="Type a message..."
          className="border-2 rounded-md w-full border-gray-400"
        />
        <button
          onClick={sendMessage}
          type="submit"
          className="bg-green-300 px-4 py-1 rounded-md"
        >
          Send
        </button>
      </div>
      <div>
        <div className="bg-white py-0.5 mt-8" />
        <p>Status: {isConnected ? "connected" : "disconnected"}</p>
        <p>Transport: {transport}</p>
      </div>
    </div>
  );
}

